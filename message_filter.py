        if has_video:
            return f"–ü–æ–ª—É—á–µ–Ω–æ –≤–∏–¥–µ–æ: {message_text}"
            
        # –§–æ—Ä–º–∞—Ç–∏—Ä—É–µ–º —Å–æ–æ–±—â–µ–Ω–∏–µ –æ –ø—Ä–æ–≥—Ä–µ—Å—Å–µ
        if any(progress in message_text for progress in self.progress_messages):
            return "–ì–µ–Ω–µ—Ä–∞—Ü–∏—è –≤–∏–¥–µ–æ –≤ –ø—Ä–æ—Ü–µ—Å—Å–µ..."
            
        return message_text 